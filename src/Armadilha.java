public class Armadilha {
    private int posicao;
    private int dano;

    public Armadilha(int posicao, int dano) {
        this.posicao = posicao;
        this.dano = dano;
    }

    public int getPosicao() {
        return posicao;
    }

    public int getDano() {
        return dano;
    }
}